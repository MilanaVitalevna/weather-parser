"""Модуль для преобразования единиц давления."""

# Коэффициент преобразования давления: 1 гПа (HPA) = 0.750064 мм рт. ст.(MMHG)
HPA_TO_MMHG_RATIO = 0.750064


def convert_pressure_to_mmhg(pressure_hpa: float) -> int:
    """
    Преобразует давление из гектопаскалей (гПа) в миллиметры ртутного столба (мм рт. ст.).

    Args:
        pressure_hpa: Давление в гектопаскалях

    Returns:
        Давление в миллиметрах ртутного столба, округленное до целого
    """
    return round(pressure_hpa * HPA_TO_MMHG_RATIO)
